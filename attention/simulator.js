    class MatrixChart {
        constructor(canvas) {
            this.canvas = canvas;
            this.ctx = canvas.getContext('2d');
            const dpr = window.devicePixelRatio || 1;
            const w = canvas.clientWidth || canvas.width;
            const h = canvas.clientHeight || canvas.height;
            canvas.width = w * dpr;
            canvas.height = h * dpr;
            this.ctx.scale(dpr, dpr);
            this.w = w; this.h = h;
            this.cellBounds = [];
            canvas.addEventListener('mousemove', e => this._onMove(e));
            canvas.addEventListener('mouseleave', () => { this.hover = null; this._redraw(); });
        }
        _onMove(e) {
            const rect = this.canvas.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            let found = null;
            for (const c of this.cellBounds) {
                if (x >= c.x && x <= c.x + c.w && y >= c.y && y <= c.y + c.h) {
                    found = c; break;
                }
            }
            if (found !== this.hover) { this.hover = found; this._redraw(); }
        }
        render(preset, mat) {
            this.preset = preset;
            this.mat = mat;
            this._redraw();
        }
        _redraw() {
            const { ctx, w, h, preset, mat } = this;
            if (!preset || !mat) return;
            ctx.clearRect(0, 0, w, h);
            const n = preset.tokens.length;
            const labelW = 80, labelH = 60;
            const cellW = (w - labelW - 20) / n;
            const cellH = (h - labelH - 20) / n;
            const startX = labelW, startY = labelH;

            this.cellBounds = [];

            // Column labels (Key)
            ctx.fillStyle = '#7c3aed';
            ctx.font = 'bold 11px -apple-system, sans-serif';
            ctx.textAlign = 'center';
            ctx.textBaseline = 'bottom';
            for (let j = 0; j < n; j++) {
                const x = startX + j * cellW + cellW / 2;
                ctx.save();
                ctx.translate(x, startY - 5);
                ctx.rotate(-Math.PI / 4);
                ctx.textAlign = 'left';
                ctx.fillText(preset.tokens[j], 0, 0);
                ctx.restore();
            }
            // Row labels (Query)
            ctx.textAlign = 'right';
            ctx.textBaseline = 'middle';
            for (let i = 0; i < n; i++) {
                const y = startY + i * cellH + cellH / 2;
                ctx.fillText(preset.tokens[i], startX - 5, y);
            }

            // Cells
            for (let i = 0; i < n; i++) {
                for (let j = 0; j < n; j++) {
                    const v = mat[i][j];
                    const x = startX + j * cellW, y = startY + i * cellH;
                    ctx.fillStyle = attentionColor(v);
                    ctx.fillRect(x, y, cellW - 1, cellH - 1);
                    if (v >= 0.15) {
                        ctx.fillStyle = v > 0.5 ? '#fff' : '#1f2937';
                        ctx.font = 'bold 10px ui-monospace, monospace';
                        ctx.textAlign = 'center';
                        ctx.textBaseline = 'middle';
                        ctx.fillText((v * 100).toFixed(0), x + cellW / 2, y + cellH / 2);
                    }
                    this.cellBounds.push({ x, y, w: cellW - 1, h: cellH - 1, i, j, v });
                }
            }

            // Hover tooltip
            if (this.hover) {
                const c = this.hover;
                ctx.strokeStyle = '#eab308';
                ctx.lineWidth = 2;
                ctx.strokeRect(c.x, c.y, c.w, c.h);
                ctx.fillStyle = '#1f2937';
                ctx.font = 'bold 12px -apple-system, sans-serif';
                ctx.textAlign = 'left';
                ctx.textBaseline = 'top';
                const tip = `${preset.tokens[c.i]} → ${preset.tokens[c.j]} : ${pctStr(c.v)}`;
                ctx.fillStyle = '#fef9e7';
                const tw = ctx.measureText(tip).width + 12;
                ctx.fillRect(5, 5, tw, 22);
                ctx.strokeStyle = '#eab308';
                ctx.strokeRect(5, 5, tw, 22);
                ctx.fillStyle = '#1f2937';
                ctx.fillText(tip, 11, 12);
            }

            // Axis labels
            ctx.fillStyle = '#9ca3af';
            ctx.font = '11px -apple-system, sans-serif';
            ctx.textAlign = 'left';
            ctx.textBaseline = 'top';
            ctx.fillText('Query (row) ↓  ×  Key (col) →', 8, h - 16);
        }
    }

