    // ---------- Matrix builder ----------
    // 每個 head 用「signals + style 的基礎分佈」建 raw score，最後 row-softmax
    function buildMatrix(preset, headIdx) {
        const n = preset.tokens.length;
        const style = HEAD_META[headIdx].style;
        const raw = Array.from({ length: n }, () => new Array(n).fill(0));

        // Baseline: 每個 head 的預設分佈風格
        for (let i = 0; i < n; i++) {
            for (let j = 0; j < n; j++) {
                raw[i][j] = baselineForStyle(style, i, j, n);
            }
        }
        // 疊加 signals
        const signals = (preset.signals[headIdx] || []);
        for (const s of signals) {
            if (s.from < n && s.to < n) {
                raw[s.from][s.to] += s.weight;
            }
        }
        // Row softmax
        const mat = Array.from({ length: n }, () => new Array(n).fill(0));
        for (let i = 0; i < n; i++) {
            const rowMax = Math.max(...raw[i]);
            const exp = raw[i].map(v => Math.exp(v - rowMax));
            const sum = exp.reduce((s, v) => s + v, 0);
            for (let j = 0; j < n; j++) mat[i][j] = exp[j] / sum;
        }
        return { raw, mat };
    }

    function baselineForStyle(style, i, j, n) {
        // seeded pseudo-random so 同一 head 每次載入都一樣
        const seedNoise = ((i * 31 + j * 17 + style.length) % 100) / 100 * 0.4;
        switch (style) {
            case 'pronoun':
            case 'subject':
            case 'object':
            case 'modifier':
                // 淡淡的自關注 + 距離衰減，讓沒 signal 的 row 不至於全零
                return (i === j ? 1.5 : 0) + Math.max(0, 1.5 - Math.abs(i - j) * 0.4) + seedNoise;
            case 'logic':
                return (i === j ? 1.0 : 0) + seedNoise;
            case 'bigram':
                // 只看左右鄰居
                if (Math.abs(i - j) === 1) return 3.0 + seedNoise;
                if (i === j) return 1.5;
                return -0.5 + seedNoise;
            case 'global':
                // 均勻散播
                return 1.0 + seedNoise * 0.5;
            case 'backref':
                // 看反方向（右邊）
                if (j > i && j - i <= 3) return 2.0 + seedNoise;
                if (i === j) return 1.0;
                return seedNoise;
        }
        return seedNoise;
    }

    // ---------- Utils ----------
    function pctStr(v) { return (v * 100).toFixed(1) + '%'; }

    // 依權重取色（顏色越深表示注意力越強）
    function attentionColor(w, alpha = 1) {
        const t = Math.min(1, Math.max(0, w));
        // 白 → 紫
        const r = Math.round(255 - t * (255 - 108));
        const g = Math.round(255 - t * (255 - 40));
        const b = Math.round(255 - t * (255 - 175));
        return `rgba(${r},${g},${b},${alpha})`;
    }

    // ---------- Scene canvas: sentence + arrows ----------
    class SceneChart {
        constructor(canvas) {
            this.canvas = canvas;
            this.ctx = canvas.getContext('2d');
            this.setupHiDPI();
            this.selectedIdx = null;
            this.tokenBounds = [];
            canvas.addEventListener('click', e => this._onClick(e));
        }
        setupHiDPI() {
            const dpr = window.devicePixelRatio || 1;
            const w = this.canvas.clientWidth || this.canvas.width;
            const h = this.canvas.clientHeight || this.canvas.height;
            this.canvas.width = w * dpr;
            this.canvas.height = h * dpr;
            this.ctx.scale(dpr, dpr);
            this.w = w; this.h = h;
        }
        _onClick(e) {
            const rect = this.canvas.getBoundingClientRect();
            const x = (e.clientX - rect.left);
            const y = (e.clientY - rect.top);
            for (const t of this.tokenBounds) {
                if (x >= t.x - t.w / 2 && x <= t.x + t.w / 2 && y >= t.y - 25 && y <= t.y + 25) {
                    this.selectedIdx = t.idx;
                    if (this.onSelect) this.onSelect(t.idx);
                    return;
                }
            }
        }
        setIntensity(v) { this.intensity = v; }
        render(preset, mat, headIdx) {
            const { ctx, w, h } = this;
            ctx.clearRect(0, 0, w, h);
            const tokens = preset.tokens;
            const n = tokens.length;
            const padX = 30, padY = 60;
            const spacing = (w - padX * 2) / (n - 1 || 1);
            // Token 定位在畫面上 30%（Reviewer round 4）：canvas 從 240 拉到 340 後
            // 若 y = h/2 會下移 50px 導致 Stage 2 的 raw 長條圖跟 token 節點重疊。
            // 定 y ≈ h * 0.30 讓：上方留空間給箭頭 + Q 泡泡、下方留 240px 給 Stage 2 三層
            const y = Math.round(h * 0.30);

            // Compute token bounds
            this.tokenBounds = tokens.map((t, i) => {
                const x = padX + i * spacing;
                ctx.font = 'bold 18px -apple-system, "Segoe UI", sans-serif';
                const tw = Math.max(46, ctx.measureText(t).width + 20);
                return { idx: i, x, y, w: tw, text: t };
            });

            // Draw arrows from selected token (before drawing tokens so tokens overlay)
            if (this.selectedIdx !== null && mat) {
                const i = this.selectedIdx;
                const row = mat[i];
                const intensity = this.intensity || 1.5;
                for (let j = 0; j < n; j++) {
                    if (j === i) continue;
                    const w0 = row[j];
                    if (w0 < 0.03) continue;   // 太細跳過
                    const from = this.tokenBounds[i];
                    const to = this.tokenBounds[j];
                    this._drawArrow(from, to, w0 * intensity, w0);
                }
            }

            // Draw tokens
            for (const tb of this.tokenBounds) {
                const isSel = tb.idx === this.selectedIdx;
                ctx.fillStyle = isSel ? '#fef3c7' : '#f3e8ff';
                ctx.strokeStyle = isSel ? '#eab308' : '#a78bfa';
                ctx.lineWidth = isSel ? 3 : 1.5;
                const rx = tb.x - tb.w / 2, ry = tb.y - 22;
                this._roundRect(rx, ry, tb.w, 44, 10);
                ctx.fill();
                ctx.stroke();
                ctx.fillStyle = '#1f2937';
                ctx.font = isSel ? 'bold 18px -apple-system, "Segoe UI", sans-serif' : '18px -apple-system, "Segoe UI", sans-serif';
                ctx.textAlign = 'center';
                ctx.textBaseline = 'middle';
                ctx.fillText(tb.text, tb.x, tb.y);
                // idx label below
                ctx.font = '10px ui-monospace, monospace';
                ctx.fillStyle = '#9ca3af';
                ctx.fillText('#' + tb.idx, tb.x, tb.y + 30);
            }

            // Instruction
            if (this.selectedIdx === null) {
                ctx.fillStyle = '#9ca3af';
                ctx.font = '13px -apple-system, sans-serif';
                ctx.textAlign = 'center';
                ctx.textBaseline = 'top';
                ctx.fillText('👆 點任一個詞看它的注意力扇向哪裡', w / 2, 10);
            } else {
                const t = tokens[this.selectedIdx];
                ctx.fillStyle = '#7c3aed';
                ctx.font = 'bold 13px -apple-system, sans-serif';
                ctx.textAlign = 'center';
                ctx.textBaseline = 'top';
                ctx.fillText(`"${t}" 在 ${HEAD_META[headIdx].name} 下的注意力去向：`, w / 2, 10);
            }
        }
        _drawArrow(from, to, thickness, weight) {
            const ctx = this.ctx;
            const dir = to.x > from.x ? 1 : -1;
            const arch = 40 + Math.abs(to.idx - from.idx) * 10;
            const cx = (from.x + to.x) / 2;
            const cy = from.y - arch;
            // Curve
            ctx.strokeStyle = attentionColor(weight, 0.8);
            ctx.lineWidth = Math.max(0.6, thickness * 6);
            ctx.lineCap = 'round';
            ctx.beginPath();
            ctx.moveTo(from.x, from.y - 22);
            ctx.quadraticCurveTo(cx, cy, to.x, to.y - 22);
            ctx.stroke();
            // Arrow head
            const hx = to.x, hy = to.y - 22;
            const angle = Math.atan2(hy - cy, hx - cx);
            const ah = 8;
            ctx.fillStyle = attentionColor(weight, 0.9);
            ctx.beginPath();
            ctx.moveTo(hx, hy);
            ctx.lineTo(hx - ah * Math.cos(angle - Math.PI / 6), hy - ah * Math.sin(angle - Math.PI / 6));
            ctx.lineTo(hx - ah * Math.cos(angle + Math.PI / 6), hy - ah * Math.sin(angle + Math.PI / 6));
            ctx.closePath();
            ctx.fill();
            // Weight label near mid
            ctx.fillStyle = '#7c3aed';
            ctx.font = 'bold 10px ui-monospace, monospace';
            ctx.textAlign = 'center';
            ctx.textBaseline = 'bottom';
            if (weight >= 0.15) ctx.fillText(pctStr(weight), cx, cy + 6);
        }
        _roundRect(x, y, w, h, r) {
            const ctx = this.ctx;
            ctx.beginPath();
            ctx.moveTo(x + r, y);
            ctx.arcTo(x + w, y, x + w, y + h, r);
            ctx.arcTo(x + w, y + h, x, y + h, r);
            ctx.arcTo(x, y + h, x, y, r);
            ctx.arcTo(x, y, x + w, y, r);
            ctx.closePath();
        }
    }

